<?php
  // Deklarasikan variabel 'string' dan berikan nilai.
  // <br> adalah HTML yang setara dengan baris baru.
  $yourstring = 'Hello World!<br>';
  // Menggunakan echo
  echo $yourstring;
  // Menggunakan print.
  print $yourstring;
  // Atau, jika Anda terbiasa dengan C, printf dapat digunakan juga.
  printf('%s', $yourstring);
?>
