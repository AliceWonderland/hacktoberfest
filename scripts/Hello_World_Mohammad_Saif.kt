//Unique way to print Hello! World program in kotlin

    fun main(args: Array<String>) {
        val hello = "H"
        val world = "W"
        val space = " "
        val exclamation = "!"
        val newline = "\n"

        val h = hello + "e" + "l" + "l" + "o"
        val w = world + "o" + "r" + "l" + "d"

        val message = h + space + w + exclamation + newline
        println(message)
    }

